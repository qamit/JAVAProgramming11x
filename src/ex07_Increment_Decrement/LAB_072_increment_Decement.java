package ex07_Increment_Decrement;

public class LAB_072_increment_Decement {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(a);  //11
        System.out.println(result); // 11


        int b = 10;
        int res= --b;
        System.out.println(b);
        System.out.println(res);



    }
}
